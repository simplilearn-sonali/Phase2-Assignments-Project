package com.simplilearn.dto;

public class Place {
    String place1;
    String place2;

    public Place(String place1, String place2) {
        this.place1 = place1;
        this.place2 = place2;
    }

    public String getPlace1() {
        return place1;
    }

    public void setPlace1(String place1) {
        this.place1 = place1;
    }

    public String getPlace2() {
        return place2;
    }

    public void setPlace2(String place2) {
        this.place2 = place2;
    }


}
